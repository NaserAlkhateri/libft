#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	c;

	c = 48;
	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	else if (n < 0)
	{
		n *= -1;
		write(fd, "-", 1);
	}
	if (n > 9)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
	else
	{
		c += n;
		write(fd, &c, 1);
	}
}
